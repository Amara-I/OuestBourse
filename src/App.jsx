// PORTFOLIO VIEW
// ══════════════════════════════════════════════════════════════
function PortfolioView({ user, stocks, notify }) {
  const getLS = (k, d) => { try { return JSON.parse(localStorage.getItem(k)) ?? d; } catch { return d; } };
  const setLS = (k, v) => localStorage.setItem(k, JSON.stringify(v));

  const [portfolio, setPortfolio] = useState(() => {
    const users = getLS("ob_users", []);
    return users.find(u => u.email === user.email)?.portfolio || [];
  });
  const [form, setForm] = useState({ ticker: "", qty: "", buyPrice: "", date: "" });
  const [showAdd, setShowAdd] = useState(false);

  const savePortfolio = (newP) => {
    const users = getLS("ob_users", []);
    const idx = users.findIndex(u => u.email === user.email);
    if (idx !== -1) { users[idx].portfolio = newP; setLS("ob_users", users); setLS("ob_user", users[idx]); }
    setPortfolio(newP);
  };

  const addPosition = () => {
    if (!form.ticker || !form.qty || !form.buyPrice) return notify("Remplissez tous les champs", "error");
    const stock = stocks.find(s => s.ticker === form.ticker.toUpperCase());
    if (!stock) return notify("Ticker introuvable", "error");
    const newPos = { ticker: form.ticker.toUpperCase(), qty: parseInt(form.qty), buyPrice: parseFloat(form.buyPrice), date: form.date || new Date().toISOString().slice(0, 10) };
    savePortfolio([...portfolio, newPos]);
    setForm({ ticker: "", qty: "", buyPrice: "", date: "" });
    setShowAdd(false);
    notify(`Position ${form.ticker.toUpperCase()} ajoutée`, "success");
  };

  const removePosition = (i) => {
    const newP = portfolio.filter((_, idx) => idx !== i);
    savePortfolio(newP);
    notify("Position supprimée", "info");
  };

  const enriched = portfolio.map(p => {
    const s = stocks.find(st => st.ticker === p.ticker);
    const currentPrice = s?.currentPrice || p.buyPrice;
    const gainLoss = (currentPrice - p.buyPrice) * p.qty;
    const gainPct = ((currentPrice - p.buyPrice) / p.buyPrice * 100).toFixed(2);
    return { ...p, currentPrice, gainLoss, gainPct, totalValue: currentPrice * p.qty, totalCost: p.buyPrice * p.qty };
  });

  const totalValue = enriched.reduce((a, b) => a + b.totalValue, 0);
  const totalCost = enriched.reduce((a, b) => a + b.totalCost, 0);
  const totalGain = totalValue - totalCost;
  const totalGainPct = totalCost > 0 ? ((totalGain / totalCost) * 100).toFixed(2) : 0;

  const chartData = enriched.map(p => ({ name: p.ticker, value: p.totalValue, gain: p.gainLoss }));

  return (
    <div style={{ animation: "fadeIn 0.4s ease" }}>
      {/* Summary Cards */}
      <div style={{ display: "grid", gridTemplateColumns: "repeat(auto-fit, minmax(180px,1fr))", gap: 16, marginBottom: 24 }}>
        {[
          { label: "Valeur du portfolio", value: `${fmt(totalValue)} FCFA`, color: C.gold },
          { label: "Coût total", value: `${fmt(totalCost)} FCFA`, color: C.silver },
          { label: "Gain / Perte", value: `${totalGain >= 0 ? "+" : ""}${fmt(Math.round(totalGain))} FCFA`, color: colorVar(totalGain) },
          { label: "Performance", value: `${totalGainPct}%`, color: colorVar(parseFloat(totalGainPct)) },
          { label: "Positions", value: portfolio.length, color: C.text },
        ].map((m, i) => (
          <div key={i} style={{ ...S.card, textAlign: "center" }}>
            <div style={{ fontSize: 11, color: C.silver, marginBottom: 6 }}>{m.label}</div>
            <div style={{ fontWeight: 700, fontSize: 16, color: m.color }}>{m.value}</div>
          </div>
        ))}
      </div>

      {/* Chart + Table */}
      {enriched.length > 0 && (
        <div style={{ display: "grid", gridTemplateColumns: "1fr 2fr", gap: 16, marginBottom: 20 }}>
          <div style={S.card}>
            <div style={{ fontWeight: 700, marginBottom: 12, fontSize: 14 }}>Répartition du portfolio</div>
            <ResponsiveContainer width="100%" height={200}>
              <BarChart data={chartData} layout="vertical">
                <CartesianGrid strokeDasharray="3 3" stroke={C.border} />
                <XAxis type="number" tick={{ fill: C.silver, fontSize: 10 }} tickFormatter={v => `${Math.round(v / 1000)}k`} />
                <YAxis dataKey="name" type="category" tick={{ fill: C.gold, fontSize: 11, fontWeight: 600 }} width={60} />
                <Tooltip contentStyle={{ background: C.panel, border: `1px solid ${C.border}`, borderRadius: 8, color: C.text }} formatter={v => [`${fmt(v)} FCFA`]} />
                <Bar dataKey="value" fill={C.brvmGreen} radius={[0, 4, 4, 0]} />
              </BarChart>
            </ResponsiveContainer>
          </div>
          <div style={S.card}>
            <div style={{ fontWeight: 700, marginBottom: 12, fontSize: 14 }}>Évolution gain/perte par position</div>
            <ResponsiveContainer width="100%" height={200}>
              <BarChart data={chartData}>
                <CartesianGrid strokeDasharray="3 3" stroke={C.border} />
                <XAxis dataKey="name" tick={{ fill: C.gold, fontSize: 11 }} />
                <YAxis tick={{ fill: C.silver, fontSize: 10 }} tickFormatter={v => fmt(v)} />
                <Tooltip contentStyle={{ background: C.panel, border: `1px solid ${C.border}`, borderRadius: 8, color: C.text }} formatter={v => [`${fmt(v)} FCFA`, "Gain/Perte"]} />
                <ReferenceLine y={0} stroke={C.border} />
                <Bar dataKey="gain" radius={[4, 4, 0, 0]} fill={C.green}
                  label={false}>{chartData.map((d, i) => <rect key={i} fill={d.gain >= 0 ? C.green : C.red} />)}</Bar>
              </BarChart>
            </ResponsiveContainer>
          </div>
        </div>
      )}

      {/* Add Position */}
      <div style={{ marginBottom: 16, display: "flex", justifyContent: "space-between", alignItems: "center" }}>
        <h3 style={{ margin: 0, color: C.gold }}>💼 Mes positions ({portfolio.length})</h3>
        <button style={S.btnPrimary} onClick={() => setShowAdd(!showAdd)}>
          {showAdd ? "✕ Annuler" : "+ Ajouter une position"}
        </button>
      </div>

      {showAdd && (
        <div style={{ ...S.card, marginBottom: 16, display: "grid", gridTemplateColumns: "repeat(auto-fit, minmax(160px,1fr))", gap: 12, animation: "fadeIn 0.3s ease" }}>
          {[
            { key: "ticker", label: "Ticker", ph: "ex: SNTS" },
            { key: "qty", label: "Quantité", ph: "ex: 100", type: "number" },
            { key: "buyPrice", label: "Prix d'achat (FCFA)", ph: "ex: 24000", type: "number" },
            { key: "date", label: "Date d'achat", ph: "", type: "date" },
          ].map(f => (
            <div key={f.key}>
              <label style={{ fontSize: 12, color: C.silver, display: "block", marginBottom: 5 }}>{f.label}</label>
              <input style={S.input} type={f.type || "text"} value={form[f.key]} onChange={e => setForm(p => ({ ...p, [f.key]: e.target.value }))} placeholder={f.ph} />
            </div>
          ))}
          <div style={{ display: "flex", alignItems: "flex-end" }}>
            <button style={{ ...S.btnPrimary, width: "100%" }} onClick={addPosition}>Ajouter</button>
          </div>
        </div>
      )}

      {enriched.length === 0 ? (
        <div style={{ ...S.card, textAlign: "center", padding: 40, color: C.silver }}>
          <div style={{ fontSize: 40, marginBottom: 12 }}>💼</div>
          <div style={{ fontSize: 15, marginBottom: 8 }}>Portfolio vide</div>
          <div style={{ fontSize: 13 }}>Ajoutez vos positions pour suivre vos investissements</div>
        </div>
      ) : (
        <div style={{ ...S.card, overflowX: "auto" }}>
          <table style={{ width: "100%", borderCollapse: "collapse", fontSize: 13 }}>
            <thead>
              <tr style={{ color: C.silver, fontSize: 11 }}>
                {["Ticker","Qté","Prix achat","Prix actuel","Valeur","Gain/Perte","Perf. %","Date","Action"].map(h => (
                  <th key={h} style={{ textAlign: "left", padding: "10px 12px", borderBottom: `1px solid ${C.border}` }}>{h}</th>
                ))}
              </tr>
            </thead>
            <tbody>
              {enriched.map((p, i) => (
                <tr key={i} style={{ borderBottom: `1px solid ${C.border}` }}>
                  <td style={{ padding: "10px 12px", color: C.gold, fontWeight: 700 }}>{p.ticker}</td>
                  <td style={{ padding: "10px 12px" }}>{fmt(p.qty)}</td>
                  <td style={{ padding: "10px 12px" }}>{fmt(p.buyPrice)}</td>
                  <td style={{ padding: "10px 12px", fontWeight: 700 }}>{fmt(p.currentPrice)}</td>
                  <td style={{ padding: "10px 12px", fontWeight: 700 }}>{fmt(Math.round(p.totalValue))}</td>
                  <td style={{ padding: "10px 12px", color: colorVar(p.gainLoss), fontWeight: 700 }}>{p.gainLoss >= 0 ? "+" : ""}{fmt(Math.round(p.gainLoss))}</td>
                  <td style={{ padding: "10px 12px", color: colorVar(parseFloat(p.gainPct)), fontWeight: 700 }}>{fmtPct(parseFloat(p.gainPct))}</td>
                  <td style={{ padding: "10px 12px", color: C.silver, fontSize: 11 }}>{p.date}</td>
                  <td style={{ padding: "10px 12px" }}>
                    <button onClick={() => removePosition(i)} style={{ ...S.btn(false), color: C.red, padding: "4px 8px", fontSize: 11 }}>✕</button>
                  </td>
                </tr>
              ))}
            </tbody>
          </table>
        </div>
      )}
    </div>
  );
}

